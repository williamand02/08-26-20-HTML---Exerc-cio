function initPage() {

    //Nome do site onde peguei o link das imagens e os nomes
    var etna = "https://www.etna.com.br/"
    cadeiras.forEach(cadeira => {
        let tr = document.createElement("tr");
        Object.keys(cadeira).forEach(function (item) {
            if (item == "foto") {
                let img = document.createElement("img");
                img.src = etna + cadeira[item];
                let td = document.createElement("td");
                td.append(img);
                tr.append(td);
                return
            }
            let p = document.createElement("p");
            p.textContent = cadeira[item]
            let td = document.createElement("td");
                td.append(p);
            tr.append(td);
        });
        tr.addEventListener('click', modalProduto);
        document.querySelector("tbody").appendChild(tr)
    });
    function modalProduto(e) {
        let display = document.querySelector(".modal-produto").style.display;
        document.querySelector(".modal-produto").style.display = display == ''? 'block':'';
        if(document.querySelector(".modal-produto").style.display == 'block'){
            let corpo = document.querySelector('.corpo-modal');
                corpo.innerHTML="";
                corpo.appendChild(this.querySelector('img'))
                corpo.appendChild(this.querySelector('p'))
        }
        // document.querySelector(".modal-produto")
        console.log(this)
        console.log("clicou")
    }
    document.querySelector(".modal-produto").addEventListener('click', modalProduto);

}
